			<h4>总则</h4>
			<p><b>第一条</b>　本附加保险合同（以下简称“本附加合同”）附加于中国大地保险个人医疗保险（C 款）或个人恶性肿瘤医疗保险（以下简称“主险”）合同。凡主险合同内容与本附加合同相关的部分，以及本附加保险条款，均为本附加合同的构成部分。</p>
			<p>本附加保险条款未尽事项，以主险保险条款为准；若本附加保险条款与主险保险条款内容冲突，则以本附加保险条款为准。</p>
			<p>主险合同效力终止，本附加合同效力亦同时终止。</p>
			<p><b>第二条</b>　本附加合同的投保人、被保险人和受益人同主险合同。</p>
			
			<h4>保险责任</h4>
			<p><b>第三条</b>　保险期间内，被保险人在等待期后（续保的不在此限）首次出现症状或者体征，经保险人指定或者认可的医疗机构的专科医生首次确诊患有本附加合同约定的恶性肿瘤（无论一种或者多种），对于被保险人按照《领取特种药品流程》（见附表一，下同）的约定领取的、同时满足下列条件的院外特种药品费用，保险人按照本附加保险条款“保险金计算方法”中的约定计算给付恶性肿瘤院外特种药品费用医疗保险金。</p>
			<p>（一）该特种药品处方是由三级公立医院的专科医生开具的；</p>
			<p>（二）该特种药品对于被保险人的当前治疗是医学必需的；</p>
			<p>（三）该特种药品是《特种药品清单》（见附表二，下同）中所列的；</p>
			<p>（四）每次购买的特种药品不超过三十日的处方剂量。</p>
			<p>保险期间届满时，该恶性肿瘤治疗仍未结束的，保险人继续承担保险责任，直至被保险人首次确诊罹患本附加合同约定的恶性肿瘤之日起第365日止（以下简称“用药保障期间”）。</p>
			
			<h4>责任免除</h4>
			<p><b>第四条　由于下列任何原因发生的恶性肿瘤院外特种药品费用，保险人不承担给付保险金的责任：</b></p>
			<p><b>（一）被保险人所患既往症及其并发症，以及其在等待期内接受检查但在等待期后确诊的疾病、症状或体征；</b></p>
			<p><b>（二）主险保险条款“责任免除”中的原因，除非为本附加合同所承保的。</b><p>
			<p><b>第五条　在下列任何情形下发生的恶性肿瘤院外特种药品费用，保险人不承担给付保险金的责任：</b><p>
			<p><b>（一）在中国大陆境外的国家或者地区接受治疗期间；</b><p>
			<p><b>（二）特种药品处方的开具与国家食品药品监督管理总局批准的该药品说明书中所列明的适应症和用法用量不符；</b><p>
			<p><b>（三）被保险人的疾病状况对申领特种药品已经产生耐药；</b></p>
			<p><b>（四）被保险人用药时长符合保险人指定或认可的慈善机构援助用药项目（以下简称“援助用药”）申请条件，但因被保险人未提交相关申请或者提交的申请材料不全，导致援助项目申请未通过；或被保险人通过保险人指定或认可的慈善机构的援助审核，但因被保险人原因未领取援助药品；</b></p>
			<p><b>（五）相关医学材料不能证明申请领取的特种药品对被保险人所罹患的恶性肿瘤有疗效；</b></p>
			<p><b>（六）仅有临床不适症状，入院诊断和出院诊断结果均不是恶性肿瘤；</b></p>
			<p><b>（七）主险保险条款“责任免除”中的情形，除非为本附加合同款所承保的。</b></p>

			<p><b>第六条　下列任何费用，保险人不承担给付保险金的责任：</b></p>
			<p><b>（一）被保险人接种预防恶性肿瘤或原位癌的疫苗费用；</b></p>
			<p><b>（二）被保险人进行基因测试以鉴定恶性肿瘤或原位癌的遗传性的费用；</b></p>
			<p><b>（三）未按照《领取特种药品流程》的约定申请领取的特种药品的费用；</b></p>
			<p><b>（四）主险保险条款“责任免除”中的费用，除非为本附加合同所承保的。</b></p>

			<h4>保险金额和保险费</h4>
			<p><b>第七条</b>　每一被保险人的保险金额由投保人与保险人协商确定，并于本附加合同中载明。</p>
			<p><b>第八条</b>　保险费由保险人在承保时计算确定。</p>

			<h4>保险金计算方法</h4>
			<p><b>第九条　保险人对于每次就诊发生的属于本保险责任范围内的恶性肿瘤院外特种药品医疗费用（以下简称“保险责任范围内医疗费用”），按照如下公式计算给付恶性肿瘤院外特种药品费用医疗保险金：</b></p>
			<p><b>恶性肿瘤院外特种药品费用医疗保险金=（保险责任范围内医疗费用- 从其他途径已获得补偿的药品费用）× 给付比例。</b></p>
			<p><b>从其他途径已获得补偿的药品费用包括但不限于从社会基本医疗保险、公费医疗、互助保险、工作单位、公益慈善机构、第三方责任人以及其他费用补偿型商业医疗保险等途径已获得补偿本保险责任范围内的药品费用。社保卡个人账户部分的支出视为个人支付，不属于从其他途径已获得补偿的药品费用。</b></p>
			<p><b>若被保险人以参加社会基本医疗保险或拥有公费医疗的身份投保，但未以参加社会基本医疗保险或拥有公费医疗的身份就诊并结算医疗费用，则保险人对被保险人的各项责任的给付比例为60%；其他情况下，给付比例为100%。</b></p>
			<p>用药保障期间内，保险人根据本附加合同针对每一被保险人累计给付的恶性肿瘤院外特种药品费用医疗保险金以本附加合同约定的保险金额为限，当达到该限额时，本保险责任终止。</p>

			<h4>保险期间与等待期</h4>
			<p><b>第十条</b>　保险期间为一年，具体起讫时间与主险合同一致，并在本附加合同中载明。</p>
			<p><b>第十一条　本附加合同的等待期与主险合同一致。</b></p>

			<h4>保险金申请</h4>
			<p><b>第十二条　被保险人按照《领取特种药品流程》领取属于《特种药品清单》中的特种药品，对于其所发生的属于本保险责任范围内的药品费用中应由保险人给付的那部分金额，经投保人投保时授权，保险人将通过第三方服务商与指定的药店直接结算，无需被保险人先行垫付，保险人不再就该部分金额给付保险金。</b></p>
			<p><b>第十三条　对于被保险人发生的不属于本保险责任范围的、应由被保险人负担但指定的药店或者第三方服务商未向被保险人收取的医疗费用，被保险人应在接到保险人或者其授权机构通知之日起的三十日内退还相应款项，否则，保险人有权向其追偿相应费用。</b></p>
			
			<h4>释义</h4>
			<p><b>症状：</b>指被保险人病后对机体生理功能异常的自身体验和感觉。</p>
			<p><b>体征：</b>指被保险人的体表或内部结构发生可以察觉的改变。</p>
			<p><b>指定或者认可的医疗机构：</b>包括保险人指定的医疗机构，以及经中华人民共和国卫生部门正式评定的二级或以上之公立医院，<b>但不包括上述医院的分院、联合病房或联合病床，精神病院，专供康复、休养、戒毒、戒酒、护理、养老等非以直接诊治病人为目的的医疗机构。</b></p>
			<p><b>专科医生：</b>专科医生应同时满足以下四项资格条件：</p>
			<p>（一）具有有效的中华人民共和国《医师资格证书》；</p>
			<p>（二）具有有效的中华人民共和国《医师执业证书》，并按期到相关部门登记注册；</p>
			<p>（三）具有有效的中华人民共和国主治医师或主治医师以上职称的《医师职称证书》；</p>
			<p>（四）在二级或二级以上医院的相应科室从事临床工作三年以上。</p>
			<p><b>首次确诊：</b>指被保险人出生之日起第一次经医疗机构确诊患有某种疾病，而不是指本合同等待期届满后第一次经医疗机构确诊患有某种疾病。</p>
			<p><b>恶性肿瘤：</b>指恶性细胞不受控制的进行性增长和扩散，浸润和破坏周围正常组织，可以经血管、淋巴管和体腔扩散转移到身体其它部位的疾病。经病理学检查结果明确诊断，临床诊断属于世界卫生组织《疾病和有关健康问题的国际统计分类》（ICD-11）的恶性肿瘤范畴。<b>原发于其他器官组织而浸润、转移的恶性肿瘤，以及首次确诊之外的恶性肿瘤不在本附加合同保障范围之内。</b></p>
			<p><b>下列疾病不在本附加合同保障范围之内：</b></p>
			<p><b>1.原位癌；</b></p>
			<p><b>2.相当于Binet 分期方案A 期程度的慢性淋巴细胞白血病；</b></p>
			<p><b>3.相当于Ann Arbor 分期方案I 期程度的何杰金氏病；</b></p>
			<p><b>4.皮肤癌（不包括恶性黑色素瘤及已发生转移的皮肤癌）；</b></p>
			<p><b>5.TNM 分期为T1N0M0 期或者更轻分期的前列腺癌；</b></p>
			<p><b>6.感染艾滋病病毒或者患艾滋病期间所患恶性肿瘤。</b></p>
			<p><b>院外：</b>指被保险人就诊医疗机构以外。</p>
			<p><b>特种药品：</b>是指符合中华人民共和国国家卫生健康委员会在适行的指导原则中对新型抗肿瘤药物的定义的药物。本附加合同涵盖合同期满日前经中国国家药品监督管理局批准并已经在中国上市的药品，且药品的适应症以中国国家药品监督管理局批准的药品说明书为准。</p>
			<p><b>医学必需：</b>指同时满足以下所有条件：</p>
			<p>（一）治疗疾病所必需的项目；</p>
			<p>（二）不超过安全、足量治疗原则的项目；</p>
			<p>（三）由医生开具的处方药；</p>
			<p>（四）非试验性的、非研究性的项目；</p>
			<p>（五）与接受治疗当地普遍接受的医疗专业实践标准一致的项目。</p>
			<p><b>处方：</b>指由注册的执业医师和在诊疗活动中为患者开具的、由取得药学专业技术职务任职资格的药学专业技术人员审核、调配、核对，并作为患者用药凭证的医疗文书。处方包括医疗机构病区用药医嘱单。</p>
			<p><b>既往症：</b>指在本合同生效前罹患的被保险人已知或应该知道的有关疾病。通常有以下情况：</p>
			<p>（一）本合同生效前，医生已有明确诊断，长期治疗未间断；</p>
			<p>（二）本合同生效前，医生已有明确诊断，治疗后症状未完全消失，有间断用药情况；</p>
			<p>（三）本合同生效前，未经医生诊断和治疗，但症状或体征明显且持续存在，以普通人医学常识应知晓。</p>
			<p><b>中国大陆境外：</b>指中华人民共和国领土之外的国家或地区，以及香港特别行政区、澳门特别行政区和台湾地区。</p>
			<p><b>耐药：</b>指以下两种情况之一：</p>
			<p>（一）实体肿瘤病灶按照RECIST（实体瘤治疗疗效评价标准）出现疾病进展，即定义为耐药。</p>
			<p>（二）非实体肿瘤（包含白血病、多发性骨髓瘤、骨髓纤维化、淋巴瘤等血液系统恶性肿瘤）在临床上常无明确的肿块或者肿块较小难以发现，经规范治疗后，按相关专业机构的指南规范，对患者的骨髓形态学、流式细胞、特种基因检测等结果进行综合评价，得出疾病进展的结论，即定义为耐药。</p>
			<p><b>保险人指定或认可的慈善机构：</b>指依法成立、符合《中华人民共和国慈善法》规定，以面向社会开展慈善活动为宗旨的非营利性组织机构。慈善机构可以采取基金会、社会团体、社会服务机构等组织形式。</p>
			<p><b>第三方服务商：</b>指经保险人授权为被保险人提供处方审核及药事服务的机构。</p>
