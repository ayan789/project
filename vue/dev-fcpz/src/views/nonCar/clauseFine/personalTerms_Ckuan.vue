        <p>2. 自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上</p>
        <p><b>（六十七）开颅手术</b></p>
        <p>指因外伤、颅内肿瘤或脑动脉瘤破裂，被保险人实际接受了在全麻下进行的颅骨切开手术，以清除脑内血肿、切除肿瘤或夹闭破裂动脉瘤的手术治疗。</p>
        <p><b>颅骨打孔手术、硬膜下血肿清除术、硬膜外血肿清除、未破裂动脉瘤预防性手术、动脉瘤栓塞手术、血管内手术、颅骨切开或切除减压术、脑积水脑脊液分流手术、经蝶骨肿瘤切除术及其他原因的开颅手术不在本合同保障范围之内。</b></p>
        <p><b>（六十八）系统性红斑狼疮性肾炎尿毒症</b></p>
        <p>系统性红斑狼疮是由多种因素引起，累及多系统的自身免疫性疾病。其特点是生成自身抗体对抗多种自身抗原。多见于育龄妇女。</p>
        <p><b>本合同所指的系统性红斑狼疮仅限于累及肾脏（经肾脏活检确认的，符合WHO 诊断标准定义III 型或III 型以上狼疮性肾炎）的系统性红斑狼疮，并且临床出现肾功能衰竭达到尿毒症期。</b></p>
        <p><b>其他类型的红斑性狼疮，如盘状狼疮、仅累及血液及关节的狼疮不在本合同保障范围之内。</b></p>
        <p><b>（六十九）席汉氏综合征</b></p>
        <p>指因产后大出血并发休克、全身循环衰竭、弥漫性血管内
          凝血导致脑垂体缺血坏死和垂体分泌激素不足，造成性腺、甲
          状腺、肾上腺皮质功能减退。必须满足下列所有条件：</p>
        <p>1. 产后大出血休克病史；</p>
        <p>2. 严重腺垂体功能破坏，破坏程度>95%；</p>
        <p>3. 影像学检查显示脑垂体严重萎缩或消失；</p>
        <p>4. 实验室检查显示：</p>
        <p>（1）垂体前叶激素全面低下；</p>
        <p>（2）性激素、甲状腺素、肾上腺皮质激素全面低下（包括生长激素、促甲状腺素、促肾上腺皮质激素、卵泡刺激素和黄体生成素）；</p>
        <p>5. 需要终身激素替代治疗以维持身体功能，持续服用各种替代激素超过一年。</p>
        <p><b>垂体功能部分低下及其他原因所致垂体功能低下不在本合同保障范围之内。</b></p>
        <p>自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上。</p>
        <p><b>（七十）神经白塞病</b></p>
        <p>白塞病是一种慢性全身性血管炎症性疾病，主要表现为复发性口腔溃疡、生殖器溃疡、眼炎及皮肤损害，可累及大血管、神经系统、消化道、肺、肾等等。累及神经系统损害的白塞病称为神经白塞病。神经白塞病必须明确诊断，并且已经造成永久不可逆的神经系统功能损害。永久不可逆的神经系统损害指被保险人持续一百八十日无法独立完成下列基本日常生活活动之一：</p>
        <p>1. 移动：自己从一个房间到另一个房间；</p>
        <p>2. 进食：自己从已准备好的碗或碟中取食物放入口中。</p>
        <p><b>（七十一）严重幼年型类风湿关节炎</b></p>
        <p>幼年型类风湿关节炎是一种儿童期发病的慢性关节炎，其特点为在高热和系统性病征出现数月后发生关节炎。</p>
        <p><b>本合同仅对实际接受了膝关节或髋关节置换手术治疗的严重的幼年型类风湿关节炎予以理赔。</b></p>
        <p><b>（七十二）重症急性出血坏死性胰腺炎开腹手术</b></p>
        <p>指被保险人被确诊为重症急性出血坏死性胰腺炎，并实际接受了外科剖腹直视手术治疗，进行坏死组织清除、坏死病灶切除或胰腺切除。</p>
        <p><b>腹腔镜手术治疗不在本合同保障范围之内。</b></p>
        <p><b>因酗酒或饮酒过量所致的急性坏死性胰腺炎不在本合同保障范围之内。</b></p>
        <p><b>（七十三）溶血性链球菌感染引起的坏疽</b></p>
        <p>由于急性溶血性链球菌感染导致广泛的皮肤、皮下组织和深层组织的坏死。必须满足以下所有条件：</p>
        <p>1. 细菌培养证实致病菌为溶血性链球菌；</p>
        <p>2. 受感染肢体被截肢（自腕关节或踝关节近端）。</p>
        <p><b>（七十四）因疾病或外伤导致智力缺陷（六周岁始理赔）</b></p>
        <p>因严重头部创伤或疾病造成被保险人智力低常（智力低于常态）或智力残疾。根据智商（IQ）智力低常分为轻度（IQ50-70）；中度（IQ35-50）；重度（IQ20-35）和极重度（IQ< 20）。<b>智商70-85 为智力临界低常，不在本合同保障范围之内。</b>智商的检测必须由保险人认可的专职心理测验工作者进行，心理测验工作者必须持有由心理测量专业委员会资格认定书。根据被保险人年龄采用对应的智力量表如韦克斯勒智力量表（儿童智力量表或成人智力量表）。</p>
        <p>理赔时必须满足下列全部条件：</p>
        <p>1. 被保险人大于或等于六周岁；</p>
        <p>2. 儿科主任医师确诊被保险人由于严重头部创伤或疾病造成智力低下；</p>
        <p>3. 专职合格心理检测工作者适时做的心理检测证实被保险人智力低常（轻度、中度、重度或极重度）；</p>
        <p>4. 被保险人的智力低常自确认日起持续一百八十日以上。</p>
        <p><b>（七十五）严重肠道疾病并发症</b></p>
        <p>严重肠道疾病或外伤导致小肠损害并发症，必须满足以下所有条件：</p>
        <p>1. 至少切除了三分之二小肠；</p>
        <p>2. 完全肠外营养支持三个月以上。</p>
        <p><b>（七十六）严重瑞氏综合征</b></p>
        <p>瑞氏综合征是线粒体功能障碍性疾病。导致脂肪代谢障碍，引起短链脂肪酸、血氨升高，造成脑水肿。主要临床表现为急性发热、反复呕吐、惊厥及意识障碍等等。肝脏活检是确诊的重要手段。瑞氏综合征需由三级医院的儿科专科医生确诊，并符合下列所有条件：</p>
        <p>1. 有脑水肿和颅内压升高的脑脊液检查和影像学检查证据；</p>
        <p>2. 血氨超过正常值的3 倍；</p>
        <p>3. 临床出现昏迷，病程至少达到疾病分期第3 期。</p>
        <p><b>（七十七）急性肺损伤（ALI）或急性呼吸窘迫综合征（ARDS）</b></p>
        <p>一种表现为无心脏衰竭的肺水肿，为创伤、脓毒血症等临床多种疾病的并发症，造成多器官衰竭，死亡率高。</p>
        <p>急性肺损伤或急性呼吸窘迫综合征必须由呼吸系统专科医师诊断，并有所有以下临床证据支持。</p>
        <p>1. 急性发作（原发疾病起病后六至七十二小时）；</p>
        <p>2.（急性发作的临床症状体征，包括呼吸急促、呼吸困难、心动过速、大汗、面色苍白及辅助呼吸肌活动加强（点头呼吸、提肩呼吸）；</p>
        <p>3. 双肺浸润影；</p>
        <p>4.PaO2/FiO2（ 动脉血压分压/ 吸入气氧分压） 低于200mmHg；</p>
        <p>5. 肺动脉嵌入压低于18mmHg，</p>
        <p>6. 临床无左房高压表现。</p>
        <p><b>（七十八）溶血性尿毒综合征</b></p>
        <p>一种由于感染导致的急性综合征，引起红细胞溶血，肾功能衰竭及尿毒症。溶血尿毒综合征必须由血液和肾脏专科医师诊断，并且符合所有以下条件：</p>
        <p>1. 实验室检查确认有溶血性贫血、血尿、尿毒症、血小板减少性紫癜；</p>
        <p>2. 因肾脏功能衰竭实施了肾脏透析治疗。</p>
        <p><b>任何非因感染导致的溶血性贫血，如：自身免疫性溶血性贫血、与红细胞末缺陷有关的溶血性贫血、红细胞酶病、血红蛋白病等，不在本合同保障范围之内。</b></p>
        <p><b>（七十九）严重登革热</b></p>
        <p>登革热是由登革热病毒引起的急性传染病，为一种自限性疾病，通常预后良好。<b>本合同仅对严重的登革热给予保障</b>，被保险人的登革热必须满足下列所有条件：</p>
        <p>1. 根据《登革热诊疗指南（2014 版）》诊断的确诊病例；</p>
        <p>2. 出现下列一种或多种严重登革热的临床表现：</p>
        <p>（1）血浆渗漏致休克或胸腔积液伴呼吸困难；</p>
        <p>（2）严重出血：消化道出血、阴道大出血、颅内出血、肉眼血尿或皮下血肿<b>（不包括皮下出血点）；</b></p>
        <p>（3） 严重器官损害或衰竭： 肝脏损伤(ALT 或AST>1000IU/L)、ARDS（急性呼吸窘迫综合征、急性心功能衰竭、急性肾功能衰竭、脑病。</p>
        <p><b>（八十）危重手足口病</b></p>
        <p>手足口病是由肠道病毒引起的急性传染病，主要症状表现为手、足、口腔等部位的斑丘疹、疱疹。</p>
        <p>危重手足口病必须满足下列所有条件：</p>
        <p>1. 病原学检查确诊为手足口病；</p>
        <p>2. 伴有所列危重并发症之一：脑膜炎、脑炎、脑脊髓炎、肺水肿或心脏衰竭；</p>
        <p>3. 接受了二周以上的住院治疗。</p>
        <p><b>（八十一）意外导致的重度面部烧伤</b></p>
        <p>指面部Ⅲ度烧伤且烧伤面积达到面部表面积的2/3 或全身体表面积的2％。</p>
        <p>体表面积根据 《中国新九分法》计算，面部总面积为全身体表面积的3%。面部面积不包括发部和颈部。</p>
        <p><b>（八十二）失去一肢及一眼（三周岁始理赔）</b></p>
        <p>被保险人因同一次意外伤害事件导致一肢体丧失和一眼视力丧失。必须满足下列所有条件：</p>
        <p>1. 一肢体丧失指任何一肢体自腕关节或踝关节近端（靠近躯干端）以上完全性断离。</p>
        <p>2. 一眼视力丧失指一只眼视力永久不可逆性丧失，患眼须满足下列至少一项条件：</p>
        <p>（1）眼球摘除；</p>
        <p>（2）矫正视力低于0.02（采用国际标准视力表，如果使用其它视力表应进行换算）；</p>
        <p>（3）视野半径小于5 度。</p>
        <p><b>除眼球摘除以外，被保险人申请理赔时年龄必须在三周岁以上，并且提供理赔当时的视力丧失诊断及检查证据。</b></p>
        <p><b>（八十三）艾森门格综合征</b></p>
        <p>指一组先天性心脏病发展的后果，特点为进行性肺动脉高压所致的动脉阻塞性病变，也称为肺动脉高压性右向左分流综合征。必须满足下列全部条件：</p>
        <p>1. 缺氧、青紫、杵状指；</p>
        <p>2. 静息状态下肺动脉平均压超过40mmHg；</p>
        <p>3. 已经造成永久不可逆性的体力活动能力受限，达到美国纽约心脏病学会心功能状态分级IV 级。</p>
        <p><b>本疾病不受本合同第八条责任免除中关于“遗传性疾病、先天性畸形、变形或染色体异常”的限制。</b></p>
        <p><b>（八十四）室壁瘤切除手术</b></p>
        <p>指因心肌梗死导致室壁瘤，实际实施了开胸开心室壁瘤切除手术治疗。</p>
        <p><b>（八十五）丝虫感染所致严重象皮病</b></p>
        <p>指因丝虫感染导致淋巴阻塞性出现严重淋巴水肿，达到国际淋巴学会淋巴肿分期第III 期，临床表现为肢体象皮肿，患肢较健肢增粗30% 以上，日常生活不能自理。</p>
        <p><b>（八十六）克- 雅氏病（CJD）</b></p>
        <p>CJD 是一种传染性海绵状脑病，临床表现为快速进行性痴呆、肌阵挛和特征性脑电图变化。本病须经三级医院的专科医师根据WHO 诊断标准明确诊断，并且被保险人永久不可逆丧失自主生活能力，无法独立完成六项基本日常生活活动中的三项或三项以上。</p>
        <p><b>（八十七）埃博拉病毒感染</b></p>
        <p>指埃博拉病毒感染导致的急性出血性传染病。埃博拉病毒病必须经国家认可的有合法资质的传染病专家确诊并且上报国家疾病控制中心接受了隔离和治疗，必须满足以下所有条件：</p>
        <p>1. 实验室检查证据证实存在埃博拉病毒感染；</p>
        <p>2. 存在持续三十日以上广泛出血的临床表现。</p>
        <p><b>埃博拉病毒感染疑似病例，在确诊之前已经死亡的不在本合同保障范围之内。</b></p>
        <p><b>（八十八）职业原因导致人类免疫缺陷病毒（HIV）感染</b></p>
        <p>被保险人的职业归属于下列职业列表内的职业，在其常规职业工作过程中遭遇外伤，或者职业需要处理血液或者其他体液时感染上人类免疫缺陷病毒（HIV）。必须满足下列全部条件：</p>
        <p>1. 感染必须发生在被保险人从事其职业工作过程中；</p>
        <p>2. 血清转化必须出现在事故发生后的六个月以内；</p>
        <p>3. 必须提供被保险人在所报事故发生后的五日以内进行的检查报告，该报告必须显示被保险人血液HIV 病毒阴性和/ 或HIV 抗体阴性；</p>
        <p>4. 必须在事故发生后的十二个月内证实被保险人体内存在HIV 病毒或者HIV 抗体。</p>
