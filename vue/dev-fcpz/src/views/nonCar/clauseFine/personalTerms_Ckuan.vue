        <p><b>（八十九）输血原因导致人类免疫缺陷病毒（HIV）感染</b></p>
        <p>被保险人因输血感染上人类免疫缺陷病毒（HIV）并且根据HIV 感染分类及AIDS 诊断标准被确诊为艾滋病（AIDS）期。满足下列全部条件：</p>
        <p>1. 在等待期届满后，被保险人因输血而感染HIV；</p>
        <p>2. 提供输血治疗的输血中心或医院出具该项输血感染属医疗责任事故的报告，或者法院终审裁定为医疗责任；</p>
        <p>3. 受感染的被保险人不是血友病患者。</p>
